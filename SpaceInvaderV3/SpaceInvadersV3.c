void Move(void){
    uint8_t alive_count = 0;

		// V4: If a hit is detected, play the explosion sound
		// TBA
	
    // V3: Move Bullet: detect hit or top of the screen. 
    if (Bullet.life == ALIVE) {

        // Top of screen
        if (Bullet.y <= BULLETH){
            Bullet.life = DEAD;
        } else {
            Bullet.y -= 3;
        }

        // Collision detection
        for(uint8_t i = 0; i < 3; i++){
            if(Enemy[i].life == ALIVE){

                if( (Bullet.x + BULLETW >= Enemy[i].x) &&
                    (Bullet.x <= Enemy[i].x + ENEMY10W) &&
                    (Bullet.y <= Enemy[i].y) &&
                    (Bullet.y >= Enemy[i].y - 10) )
                {
                    // Enemy is hit
                    Enemy[i].life = DEAD;
                    Enemy[i].image = SmallExplosion0;
                    ExplosionTimer[i] = EXPLOSION_DURATION; // << NEW >>
                    Bullet.life = DEAD;

                    break;
                }
            }
        }
    }

    // Handle Animation & Movement 
    static uint8_t posture = CLOSE;

    for (uint8_t i = 0; i < 3; i++){

        if (Enemy[i].life == ALIVE){
            alive_count++;

            Enemy[i].x += 1;

            if (Enemy[i].x + ENEMY10W >= 84){
                Enemy[i].life = DEAD;
                ExplosionTimer[i] = EXPLOSION_DURATION;
                Enemy[i].image = SmallExplosion0;
                continue;
            }

            // normal posture swap
            if (posture == CLOSE){
                Enemy[i].image = SmallEnemyPointB[i];
            } else {
                Enemy[i].image = SmallEnemyPointA[i];
            }
        }
        else { 
            // ---- EXPLOSION COUNTDOWN ----
            if (ExplosionTimer[i] > 0){
                ExplosionTimer[i]--;

                // once explosion finishes, remove enemy from drawing area
                if (ExplosionTimer[i] == 0){
                    Enemy[i].x = 200; // push offscreen
                }
            }
        }
    }

    // Flip posture
		if(posture == CLOSE){
			posture = OPEN;
		}
		else{
			posture = CLOSE;
		}

    // V2: Read ADC and update player ship position: only x coordinate will be changed. 
    uint16_t adc = ADC1SS3_In();
    PlayerShip.x = ADCValue_To_X_AXIS(adc, 84 - PLAYERW);

    // Game Over only when ALL explosions are done
    if (alive_count == 0){
        bool explosions_done = true;
        for(uint8_t i = 0; i < 3; i++){
            if (ExplosionTimer[i] > 0){
                explosions_done = false;
                break;
            }
        }
        if (explosions_done){
            game_s = OVER;
        }
    }
}



// Update the screen: 
// clear display and update the screen with the 
// current positions of all sprites that are alive.
void Draw(void){
  uint8_t i;

  if (game_s == OVER) return;

  Nokia5110_ClearBuffer();

 // V1:Update live enemies positions in display buffer
	
  for(i=0;i<3;i++){

      // explosion or alive 
      if((Enemy[i].life == ALIVE) || (ExplosionTimer[i] > 0)){
          if(Enemy[i].image != NULL){
              Nokia5110_PrintBMP(Enemy[i].x, Enemy[i].y, Enemy[i].image, 0);
          }
      }
      // if explosionTimer=0 and life=DEAD ? do NOT draw
  }

  // V2: Update the playership position in display buffer:
  if(PlayerShip.life == ALIVE){
      Nokia5110_PrintBMP(PlayerShip.x, PlayerShip.y, PlayerShip.image, 0);
  }

 // V3: Update the bullet position in display buffer if there is one.
  if(Bullet.life == ALIVE){
      Nokia5110_PrintBMP(Bullet.x, Bullet.y, Bullet.image, 0);
  }
	
	// V4: if bullet is just activated, play the shoot sound here
	

  Nokia5110_DisplayBuffer();
}


// Control screen refresh rate. 
void SysTick_Handler(void){
	// signal time to refresh
	time_to_draw = true;
}

void GPIOPortF_Handler(void){    // called on release of either SW1 or SW2
	// take care of button debounce
	for(uint8_t i = 0; i < 100; i++);
	
	// SW1: shoot a bullet if there is none
  
	if(GPIO_PORTF_RIS_R & SW1){
		GPIO_PORTF_ICR_R = SW1;
		GPIO_PORTF_DATA_R = GREEN;
		
		if(Bullet.life == DEAD){
			Bullet.life = ALIVE;
			
			Bullet.x = PlayerShip.x + (PLAYERW/2) - (BULLETW/2);
			Bullet.y = PlayerShip.y - 1;
			Bullet.image = Missile0;
			
		}
	
	}
	// SW2:start the game, change the game status to ON
	if(GPIO_PORTF_RIS_R & SW2){
		GPIO_PORTF_ICR_R = SW2;
		game_s = ON;
		GPIO_PORTF_DATA_R = RED;
		
	}
}

